  methods: {
    gotop() {
      document.documentElement.scrollTop = document.body.scrollTop = 0;
    },
    handleScroll() {
      this.scrollTop = document.documentElement.scrollTop;
    }
  },

  destroyed() {
    window.removeEventListener("scroll", this.handleScroll);
  }
};
